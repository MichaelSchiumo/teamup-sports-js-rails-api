class Teams {
  constructor(id, name, color, rank) {
    this.id = id;
    this.teamName = name;
    this.teamColor = color;
    this.teamRank = rank;
  }
}

class Players {
  constructor(id, name, position, number, captain, hometown) {
    this.id = id;
    this.name = name;
    this.position = position;
    this.number = number;
    this.captain = captain;
    this.hometown = hometown;
  }
}
